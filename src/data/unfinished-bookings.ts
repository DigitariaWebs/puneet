// Helpers used by both pages
export function getUnfinishedBookingsForFacility(
  facilityId: number,
): UnfinishedBooking[] {
  return unfinishedBookings.filter((ub) => ub.facilityId === facilityId);
}

export function getUnfinishedBookingsForCustomer(
  clientId: number,
): UnfinishedBooking[] {
  return unfinishedBookings.filter((ub) => ub.clientId === clientId);
}

